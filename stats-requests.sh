#!/bin/sh
#------------------------------------------------------------------------------
# $Id: stats-requests.sh,v 1.1 2005/11/21 10:10:56 pbui Exp pbui $
#------------------------------------------------------------------------------

LOG="$1";
IDT="Request:";

RQS="`grep $IDT $LOG | cut -d ':' -f 2 | sort | uniq | sed 's/ /__/g'`";

for RQ in $RQS; do
    ARQ=`echo "$RQ" | sed 's/__//' | sed 's/__/ /g' | cut -d '?' -f 1`;
    echo -n "$ARQ = ";
    echo `grep "$ARQ" $LOG | wc -l`;
done

